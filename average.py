#!/usr/bin/env python
round1 = int(input("Enter score for round 1: "))
round2 = int(input("Enter score for round 2: "))
round3 = int(input("Enter score for round 3: "))
average = (round1 + round2 + round3) / 3
print ("the average score is: ", average)
# at first glance at the code it ask you to input a number for each round
# After the 3 inputs it will take the numbers and divide them by 3
# Finally it will display the result with the average score is
# could be used if a boxing match only lasts 3 rounds
